local function GET_ITEM_VALUE(ITEM_NAME, IS_CHROMA)
    if IS_CHROMA then
        local CHROMA_NAME = "Chroma " .. ITEM_NAME
        if ancientValues and ancientValues[CHROMA_NAME] then
            return ancientValues[CHROMA_NAME]
        end
    end
    if ancientValues then
        return ancientValues[ITEM_NAME]
    end
    return nil -- Or some other default value if ancientValues is not found
end
 
local function UPDATE_OFFER_VALUES()
    local YOUR_TOTAL_VALUE = 0
    for I = 1, 4 do
        local NEW_ITEM = YOC:WaitForChild("NewItem" .. I, 5)
        if NEW_ITEM and NEW_ITEM.Visible then
            local ITEM_NAME_OBJ = NEW_ITEM:WaitForChild("ItemName", 5)
            local AMOUNT_CONT = NEW_ITEM:WaitForChild("Container", 5)
            local TAGS = NEW_ITEM:WaitForChild("Tags", 5)
            local CHROMA_TAG = TAGS:WaitForChild("Chroma", 5)
 
            if ITEM_NAME_OBJ and AMOUNT_CONT and TAGS and CHROMA_TAG then
                local ITEM_NAME_LBL = ITEM_NAME_OBJ:WaitForChild("Label", 5)
                local AMOUNT_LBL = AMOUNT_CONT:WaitForChild("Amount", 5)
                local IS_CHROMA = CHROMA_TAG.Visible
 
                if ITEM_NAME_LBL and ITEM_NAME_LBL.Text ~= "" then
                    local ITEM_NAME = ITEM_NAME_LBL.Text
                    local AMOUNT = 1
 
                    if AMOUNT_LBL and AMOUNT_LBL.Text ~= "" then
                        local AMOUNT_TEXT = AMOUNT_LBL.Text
                        if string.sub(AMOUNT_TEXT, 1, 1) == "x" then
                            local AMOUNT_NUM = tonumber(string.sub(AMOUNT_TEXT, 2))
                            if AMOUNT_NUM then
                                AMOUNT = AMOUNT_NUM
                            end
                        end
                    end
 
                    local ITEM_VALUE = GET_ITEM_VALUE(ITEM_NAME, IS_CHROMA)
                    if ITEM_VALUE then
                        YOUR_TOTAL_VALUE = YOUR_TOTAL_VALUE + (ITEM_VALUE * AMOUNT)
                    end
                end
            end
        end
    end
    YOT.Text = "Value: " .. YOUR_TOTAL_VALUE
 
    local THEIR_TOTAL_VALUE = 0
    for I = 1, 4 do
        local NEW_ITEM = TOC:WaitForChild("NewItem" .. I, 5)
        if NEW_ITEM and NEW_ITEM.Visible then
            local ITEM_NAME_OBJ = NEW_ITEM:WaitForChild("ItemName", 5)
            local AMOUNT_CONT = NEW_ITEM:WaitForChild("Container", 5)
            local TAGS = NEW_ITEM:WaitForChild("Tags", 5)
            local CHROMA_TAG = TAGS:WaitForChild("Chroma", 5)
 
            if ITEM_NAME_OBJ and AMOUNT_CONT and TAGS and CHROMA_TAG then
                local ITEM_NAME_LBL = ITEM_NAME_OBJ:WaitForChild("Label", 5)
                local AMOUNT_LBL = AMOUNT_CONT:WaitForChild("Amount", 5)
                local IS_CHROMA = CHROMA_TAG.Visible
 
                if ITEM_NAME_LBL and ITEM_NAME_LBL.Text ~= "" then
                    local ITEM_NAME = ITEM_NAME_LBL.Text
                    local AMOUNT = 1
 
                    if AMOUNT_LBL and AMOUNT_LBL.Text ~= "" then
                        local AMOUNT_TEXT = AMOUNT_LBL.Text
                        if string.sub(AMOUNT_TEXT, 1, 1) == "x" then
                            local AMOUNT_NUM = tonumber(string.sub(AMOUNT_TEXT, 2))
                            if AMOUNT_NUM then
                                AMOUNT = AMOUNT_NUM
                            end
                        end
                    end
 
                    local ITEM_VALUE = GET_ITEM_VALUE(ITEM_NAME, IS_CHROMA)
                    if ITEM_VALUE then
                        THEIR_TOTAL_VALUE = THEIR_TOTAL_VALUE + (ITEM_VALUE * AMOUNT)
                    end
                end
            end
        end
    end
    TOT.Text = "Value: " .. THEIR_TOTAL_VALUE .. ""
 
    if THEIR_TOTAL_VALUE > 0 then
        local PROFIT = THEIR_TOTAL_VALUE - YOUR_TOTAL_VALUE
        YOT.Text = YOT.Text .. " (Profit: " .. PROFIT .. ")"
    end
end
 
-- Run the update function initially
UPDATE_OFFER_VALUES()
 
-- Listen for changes in Your Offer
for I = 1, 4 do
    local NEW_ITEM = YOC:WaitForChild("NewItem" .. I, 5)
    if NEW_ITEM then
        NEW_ITEM:GetPropertyChangedSignal("Visible"):Connect(UPDATE_OFFER_VALUES)
        local ITEM_NAME_OBJ = NEW_ITEM:WaitForChild("ItemName", 5)
        local AMOUNT_CONT = NEW_ITEM:WaitForChild("Container", 5)
        local TAGS = NEW_ITEM:WaitForChild("Tags", 5)
        if ITEM_NAME_OBJ and AMOUNT_CONT and TAGS then
            local ITEM_NAME_LBL = ITEM_NAME_OBJ:WaitForChild("Label", 5)
            local AMOUNT_LBL = AMOUNT_CONT:WaitForChild("Amount", 5)
            local CHROMA_TAG = TAGS:WaitForChild("Chroma", 5)
            if ITEM_NAME_LBL then
                ITEM_NAME_LBL:GetPropertyChangedSignal("Text"):Connect(UPDATE_OFFER_VALUES)
            end
            if AMOUNT_LBL then
                AMOUNT_LBL:GetPropertyChangedSignal("Text"):Connect(UPDATE_OFFER_VALUES)
            end
            if CHROMA_TAG then
                CHROMA_TAG:GetPropertyChangedSignal("Visible"):Connect(UPDATE_OFFER_VALUES)
            end
        end
    end
end
 
