local function fadeIn(object, prop, from, to, step, delay)
    for i = from, to, step do
        object[prop] = i
        task.wait(delay)
    end
    object[prop] = to
end

local function slide(object, prop, direction, steps, delay)
    for _ = 1, steps do
        object[prop] = object[prop] + direction
        task.wait(delay)
    end
end

